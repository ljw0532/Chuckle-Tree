package com.company.cktree.authentications;

public class AuthenDTO {
	private int authen_no;
	private String authen_code;
	private String m_phone;
	private String m_name;
	
	public int getAuthen_no() {
		return authen_no;
	}
	public void setAuthen_no(int authen_no) {
		this.authen_no = authen_no;
	}
	public String getAuthen_code() {
		return authen_code;
	}
	public void setAuthen_code(String authen_code) {
		this.authen_code = authen_code;
	}
	public String getM_phone() {
		return m_phone;
	}
	public void setM_phone(String m_phone) {
		this.m_phone = m_phone;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	
	
}
